/*----------------------------------------------------------------------------------------------------------*/
/*  S T R U C T S                                                                                           */
/*----------------------------------------------------------------------------------------------------------*/
struct PalStats: Codable {
    
    /*------------------------------------------------------------------------------------------------------*/
    /*  V A R I A B L E   H A N D L I N G                                                                   */
    /*------------------------------------------------------------------------------------------------------*/
    let statHP: Int
    let statAttack: Attack
    let statDefense: Int
    let statSpeed: Speed
    let statStamina: Int
    let statSupport: Int
    let statFood: Int
}

struct Attack: Codable {
    
    /*------------------------------------------------------------------------------------------------------*/
    /*  V A R I A B L E   H A N D L I N G                                                                   */
    /*------------------------------------------------------------------------------------------------------*/
    let melee: Int
    let ranged: Int
}

struct Speed: Codable {
    
    /*------------------------------------------------------------------------------------------------------*/
    /*  V A R I A B L E   H A N D L I N G                                                                   */
    /*------------------------------------------------------------------------------------------------------*/
    let ride: Int
    let run: Int
    let walk: Int
}

/*----------------------------------------------------------------------------------------------------------*/
/*  C O D I N G   K E Y S                                                                                   */
/*----------------------------------------------------------------------------------------------------------*/
enum StatsCodingKeys: String, CodingKey {
    case statHP = "hp"
    case statAttack = "attack"
    case statDefense = "defense"
    case statSpeed = "speed"
    case statStamina = "stamina"
    case statSupport = "support"
    case statFood = "food"
}
