import SwiftUI

/*----------------------------------------------------------------------------------------------------------*/
/*  S T R U C T S                                                                                           */
/*----------------------------------------------------------------------------------------------------------*/
struct PalCharacterCard: View {
    
    /*------------------------------------------------------------------------------------------------------*/
    /*  V A R I A B L E   H A N D L I N G                                                                   */
    /*------------------------------------------------------------------------------------------------------*/
    let pal: PalCharacter

    /*------------------------------------------------------------------------------------------------------*/
    /*  I N I T                                                                                             */
    /*------------------------------------------------------------------------------------------------------*/
    init(pal: PalCharacter) {
        self.pal = pal
    }

    /*------------------------------------------------------------------------------------------------------*/
    /*  U I   H A N D L I N G                                                                               */
    /*------------------------------------------------------------------------------------------------------*/
    var body: some View {
        NavigationLink(destination: PalDetailView(pal: pal)) {
            VStack {
                Text(pal.palKey)
                    .padding(.top)
                Image(pal.palKey)
                    .resizable()
                    .aspectRatio(1, contentMode: .fit)
                    .frame(width: 75, height: 75)
                    .padding()
                Text(pal.palName)
                    .font(.subheadline)
                    .padding(.bottom)
            }
            .background(Color.white)
            .cornerRadius(10)
            .shadow(radius: 10)
            .padding()
        }
    }
}
